def _fetch_company_id_map(conn: sqlite3.Connection, short_names: list[str]) -> dict[str, int]:
    placeholders = ",".join("?" for _ in short_names)
    cur = conn.cursor()
    cur.execute(
        f"SELECT id, short_name FROM companies WHERE short_name IN ({placeholders})",
        short_names,
    )
    return {str(row[1]): int(row[0]) for row in cur.fetchall()}


